package org.gnome.gtk

import gtk3.*
import kotlinx.cinterop.CPointer
import kotlinx.cinterop.reinterpret

///////////////////////////////////////////////////////////////////////////
// Type
///////////////////////////////////////////////////////////////////////////

typealias Box = CPointer<GtkBox>

///////////////////////////////////////////////////////////////////////////
// Conversion
///////////////////////////////////////////////////////////////////////////

val Box.asContainer: Container
    get() = reinterpret()

val Box.asWidget: Widget
    get() = reinterpret()

val Box.asObject: Object
    get() = reinterpret()
