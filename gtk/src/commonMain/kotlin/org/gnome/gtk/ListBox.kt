///////////////////////////////////////////////////////////////////////////
// Conversion
///////////////////////////////////////////////////////////////////////////

val ListBox.asContainer: Container
    get() = reinterpret()

val ListBox.asWidget: Widget
    get() = reinterpret()

val ListBox.asObject: Object
    get() = reinterpret()
